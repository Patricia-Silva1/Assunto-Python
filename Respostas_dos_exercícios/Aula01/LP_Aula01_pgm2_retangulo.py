# Calcular área do retângulo
# 
print ('----- INÍCIO DA EXECUÇÃO -----')
# ----- entrada de dados
base = float(input('Informe o tamanho da base do retângulo: '))
altura = float(input('Informe o tamanho da altura do retângulo: '))
# ----- calculanso a área
area = base * altura
# ----- exibindo resultado
print ('RESPOSTA: A área do retângulo é:', area)
print ('----- FIM DA EXECUÇÃO -----')